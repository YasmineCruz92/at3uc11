    private void tabelaProdutosvendidosAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_tabelaProdutosvendidosAncestorAdded
 ProdutosDAO produtosdao = new ProdutosDAO();
        List<ProdutosDTO> listaProdutosVendidos = produtosdao.listarProdutosVendidos();
        
        DefaultTableModel model = (DefaultTableModel) tabelaProdutosvendidos.getModel();
        model.setRowCount(0);
        
        for (ProdutosDTO produto : listaProdutosVendidos) {
            Object[] row = new Object[]{
                produto.getId(),
                produto.getNome(),
                produto.getValor(),
                produto.getStatus()
            };
            model.addRow(row);
        }    }//GEN-LAST:event_tabelaProdutosvendidosAncestorAdded

